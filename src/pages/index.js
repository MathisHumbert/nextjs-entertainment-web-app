import React, { useEffect } from 'react';
import { getSession } from 'next-auth/react';
import Head from 'next/head';
import { ObjectId } from 'mongodb';

import { useAppContext } from '../context/appContext';
import Navbar from '../components/Navbar';
import SearchInput from '../components/SearchInput';
import Slider from '../components/Slider';
import MoviesList from '../components/MoviesList';
import { MainContainer, SecondaryContainer } from '../styles/components';
import { connectToDatabase } from '../services/mongodb';

const Home = ({ serverData = [], serverBookmarked = [] }) => {
  const { data, inputValue, setDataOnMount } = useAppContext();

  useEffect(() => {
    setDataOnMount(serverData, serverBookmarked);
  }, []);

  return (
    <>
      <Head>
        <title>Home</title>
        <meta name='description' content='Generated by create next app' />
        <link rel='icon' href='/favicon.ico' />
      </Head>

      <MainContainer>
        <Navbar />
        <SecondaryContainer>
          <div>
            <SearchInput placeholder={'Search for movies or TV series'} />
            {inputValue ? (
              <MoviesList
                data={data}
                title={`Found ${data.length > 0 ? data.length : 'no'} result${
                  data.length > 1 ? 's' : ''
                } for '${inputValue}'`}
              />
            ) : (
              <>
                <Slider data={serverData} />
                <MoviesList
                  data={serverData.filter(
                    (movie) => movie.isTrending === false
                  )}
                  title='Recommended for you'
                />
              </>
            )}
          </div>
        </SecondaryContainer>
      </MainContainer>
    </>
  );
};

export async function getServerSideProps(context) {
  const session = await getSession(context);
  if (!session) {
    return {
      redirect: {
        permanent: false,
        destination: '/login',
      },
    };
  }

  const { db } = await connectToDatabase();
  const serverData = await db.collection('movies').find().toArray();
  const serverBookmarked = await db
    .collection('bookmarked')
    .find({ userId: ObjectId(session.id) })
    .toArray();

  return {
    props: {
      serverData: serverData.map((item) => ({
        _id: item._id.toString(),
        title: item.title,
        category: item.category,
        isBookmarked: item.isBookmarked,
        isTrending: item.isTrending,
        rating: item.rating,
        year: item.year,
      })),
      serverBookmarked: serverBookmarked.map((item) => item.movieId.toString()),
    },
  };
}

export default Home;
